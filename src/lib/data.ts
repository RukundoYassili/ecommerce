export const formatPrice = (price: number) => {
  // Use en-US to guarantee consistent formatting (commas for thousands) across Server and Client, 
  // preventing hydration mismatch where Node output "RWF" and Chrome output "RF".
  return `RWF ${price.toLocaleString('en-US')}`;
};

